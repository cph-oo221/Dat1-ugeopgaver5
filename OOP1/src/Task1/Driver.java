package Task1;

public class Driver
{

    // 2 private fields:
    private String name;
    private int age;

    // Constructor
    public Driver(String name, int age)
    {
        this.name = name;
        this.age = age;
    }

    // getter
    public String getName()
    {
        return name;
    }

    public int getAge()
    {
        return age;
    }
}
